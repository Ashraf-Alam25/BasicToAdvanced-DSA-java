public class relational_operators {
    public static void main(String[] args) {
        int a = 5;
        int b = 5;
        int c = 6;
        System.out.println(a==b);
        System.out.println(a==c);
        System.out.println(a!=b);
        System.out.println(a!=c);
        System.out.println(a<b);
        System.out.println(a>b);
    }
    
}
