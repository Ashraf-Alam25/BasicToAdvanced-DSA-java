public class data_types {
    public static void main (String [] args){
        byte b = 127;
        System.out.println(b);
        char ch = 'a'; /*  use only one char at a time  */
        System.out.println(ch);
        boolean  bl = false;
        System.out.println(bl);
        float price = 10.5f; /* in the double we are use directly like this 100.5  */
        System.out.println(price);
        int in = 100; /* long = in this long use large number likhe 8 bytes */
        System.out.println(in);
        short s = 5588;
        System.out.println(s);
       


    }
    
}
